package Work;

public class Calculator {
    private int num1;
    private int num2;

    public Calculator(){
        this.num1 = num1;
        this.num2 = num2;
    }

    public void add(int num1, int num2){
        System.out.println(num1 + num2);
    }
    public void min(int num1,int num2){
        System.out.println(num1 - num2);
    }
    public void mult(int num1,int num2){
        System.out.println(num1 * num2);
    }
    public void divid(int num1,int num2){
        System.out.println(num1 / num2);
    }
}

